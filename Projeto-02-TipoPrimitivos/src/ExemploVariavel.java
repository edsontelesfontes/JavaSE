
public class ExemploVariavel {

	public static void main(String[] args) {
		
		int idade;
		double salario;
		boolean fumante;
		
		idade = 26;
		salario = 1320.15;
		fumante = false;
		
		System.out.println(idade);
		System.out.println(salario);
		System.out.println(fumante);
		
		System.out.println("Idade -> " + idade);
	}
}
